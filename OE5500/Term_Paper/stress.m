function S = stress(p, e, E, v, Q)
    A = (0.5)*det([p(e(1),:) 1; p(e(2),:) 1; p(e(3),:) 1;]);
    D = (E/(1-(v^2))).*[1 v 0; v 1 0; 0 0 (0.5)*(1-v);];
    
    y32 = p(e(3),2) - p(e(2),2);
    x32 = p(e(3),1) - p(e(2),1);
    
    y31 = p(e(3),2) - p(e(1),2);
    x31 = p(e(3),1) - p(e(1),1);
    
    y21 = p(e(2),2) - p(e(1),2);
    x21 = p(e(2),1) - p(e(1),1);

    
    B = (0.5*A)*[y32 0 -y31 0 y21 0; 0 -x32 0 x31 0 -x21; -x32 y32 x31 -y31 -x21 y21;];
    
    S = D*(B*Q');
    
    
    S = S';
   
end

